/**
 * Pre-render syntax-highlighted HTML for every snippet in
 * `src/lib/playgrounds/snippets.ts` so we don't ship Shiki on the client.
 *
 * Output: `src/lib/playgrounds/highlighted-snippets.ts`, a static map
 * `{ [slug]: Array<{ title, lang, code, html }> }`. The pages consume `html`
 * via `{@html}`, with `code` retained for the clipboard copy button.
 *
 * Re-runs at build time via the `prebuild` script. Run manually with:
 *   node apps/docs/scripts/build-highlighted-snippets.mjs
 */
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';
import { readFile, writeFile } from 'node:fs/promises';
import { createHighlighter } from 'shiki';

const __dirname = dirname(fileURLToPath(import.meta.url));
const SRC = resolve(__dirname, '../src/lib/playgrounds/snippets.ts');
const OUT = resolve(__dirname, '../src/lib/playgrounds/highlighted-snippets.ts');

/**
 * Snippets are authored as TS exports. We parse them by dynamically importing
 * the source — Node can read TS via the `--experimental-strip-types` flag on
 * 22+, but to avoid that requirement we do a tiny eval-loader instead.
 */
async function loadSnippets() {
  const src = await readFile(SRC, 'utf-8');
  // Strip `export` and the type annotations, leaving plain JS objects.
  // We only need to extract the literal value of `SNIPPETS` and `DEFAULT_SNIPPETS`.
  const js = src
    .replace(/export\s+interface\s+Snippet\s*{[^}]*}/m, '')
    .replace(/export\s+const\s+SNIPPETS:\s*[^=]+=/, 'const SNIPPETS =')
    .replace(/export\s+const\s+DEFAULT_SNIPPETS:\s*[^=]+=/, 'const DEFAULT_SNIPPETS =')
    .replace(/as\s+const\s*;/g, ';');
  // Evaluate in a sandboxed function so we can recover the two arrays.
  // eslint-disable-next-line no-new-func
  const factory = new Function(`${js}\nreturn { SNIPPETS, DEFAULT_SNIPPETS };`);
  return factory();
}

const { SNIPPETS, DEFAULT_SNIPPETS } = await loadSnippets();

// Kanagawa is a Japanese-inspired theme — pairs naturally with Kumiki's
// paper / sumi / shu aesthetic. Lotus (light) leans warm; Wave (dark) keeps
// the same hue family but pivots to twilight.
const highlighter = await createHighlighter({
  themes: ['kanagawa-lotus', 'kanagawa-wave'],
  langs: ['svelte', 'typescript', 'javascript', 'bash', 'json', 'html', 'css'],
});

const langMap = {
  svelte: 'svelte',
  ts: 'typescript',
  js: 'javascript',
  bash: 'bash',
  sh: 'bash',
  json: 'json',
  html: 'html',
  css: 'css',
};

function highlight(code, lang) {
  const shikiLang = langMap[lang] ?? 'plaintext';
  return highlighter.codeToHtml(code, {
    lang: shikiLang,
    themes: { light: 'kanagawa-lotus', dark: 'kanagawa-wave' },
    defaultColor: false,
  });
}

const escape = (s) => s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

const renderSet = (set) =>
  '[' +
  set
    .map(
      (s) =>
        `{ title: \`${escape(s.title)}\`, lang: '${s.lang}', code: \`${escape(s.code)}\`, html: \`${escape(highlight(s.code, s.lang))}\` }`,
    )
    .join(', ') +
  ']';

let out = `/* AUTO-GENERATED by scripts/build-highlighted-snippets.mjs. Do not edit. */\n`;
out += `export interface HighlightedSnippet {\n  readonly title: string;\n  readonly lang: 'svelte' | 'ts' | 'bash';\n  readonly code: string;\n  readonly html: string;\n}\n\n`;
out += `export const SNIPPETS: Record<string, ReadonlyArray<HighlightedSnippet>> = {\n`;
for (const [slug, set] of Object.entries(SNIPPETS)) {
  out += `  '${slug}': ${renderSet(set)},\n`;
}
out += `};\n\n`;
out += `export const DEFAULT_SNIPPETS: ReadonlyArray<HighlightedSnippet> = ${renderSet(DEFAULT_SNIPPETS)};\n`;

await writeFile(OUT, out, 'utf-8');

highlighter.dispose();

const totalSnippets =
  Object.values(SNIPPETS).reduce((n, set) => n + set.length, 0) + DEFAULT_SNIPPETS.length;
console.log(`Highlighted ${totalSnippets} snippets → ${OUT.replace(process.cwd() + '/', '')}`);
