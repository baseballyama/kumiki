#!/usr/bin/env node
/**
 * Build apps/docs/static/llms-full.txt — the inlined LLM-friendly variant of
 * /llms.txt per https://llmstxt.org/.
 *
 * Walks every Kumiki package, pulls:
 *   - name + description from package.json
 *   - first JSDoc block from src/index.ts (the @when-to-use / @anti-pattern
 *     / @see surface that check-jsdoc.mjs already enforces)
 *   - exported type names from dist/index.d.mts when present
 *
 * Concatenates everything into one text file the LLM can consume in a
 * single shot, with stable section dividers.
 *
 * Run via:
 *   node apps/docs/scripts/build-llms-full.mjs
 *
 * Output goes to apps/docs/static/llms-full.txt and is served at
 * /llms-full.txt by SvelteKit's static handling.
 */

import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs';
import { join, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const SCRIPT_DIR = dirname(__filename);
const ROOT = join(SCRIPT_DIR, '..', '..', '..');
const PACKAGES = join(ROOT, 'packages');
const OUT = join(SCRIPT_DIR, '..', 'static', 'llms-full.txt');

/** Walk packages/ and yield every package.json. */
function* findPackageJsons(dir) {
  let entries;
  try {
    entries = readdirSync(dir, { withFileTypes: true });
  } catch {
    return;
  }
  for (const e of entries) {
    if (e.name === 'node_modules' || e.name === 'dist') continue;
    const p = join(dir, e.name);
    if (e.isSymbolicLink()) continue;
    if (e.isDirectory()) yield* findPackageJsons(p);
    else if (e.name === 'package.json') yield p;
  }
}

/** Pull the first /** … *\/ block from a TS file. */
function firstJSDoc(text) {
  const m = text.match(/\/\*\*[\s\S]*?\*\//);
  if (!m) return '';
  return m[0]
    .split('\n')
    .map((line) => line.replace(/^\s*\*\/?$/, '').replace(/^\s*\*\s?/, ''))
    .join('\n')
    .replace(/^\/\*\*\s*/, '')
    .trim();
}

/** Pull exported names from a .d.mts/.d.ts file (cheap regex — good enough for an inventory). */
function exportedNames(dtsText) {
  const names = new Set();
  const patterns = [
    /export\s+(?:declare\s+)?(?:function|class|const|let|var|enum|interface|type)\s+([A-Za-z_$][\w$]*)/g,
    /export\s*\{\s*([^}]+)\s*\}/g,
  ];
  for (const re of patterns) {
    let m;
    while ((m = re.exec(dtsText)) !== null) {
      const captured = m[1] ?? '';
      for (const tok of captured.split(',')) {
        const name = tok
          .replace(/\s+as\s+\w+/, '')
          .replace(/^type\s+/, '')
          .trim();
        if (/^[A-Za-z_$][\w$]*$/.test(name)) names.add(name);
      }
    }
  }
  return [...names].sort();
}

const sections = [];
sections.push('# Kumiki — full reference (machine-readable)');
sections.push('');
sections.push(
  '> Per https://llmstxt.org/. This file is generated by apps/docs/scripts/build-llms-full.mjs.',
);
sections.push(
  '> Each package gets a section with its description, first JSDoc block, and exported type names.',
);
sections.push('');

const seen = [];
for (const pkgPath of findPackageJsons(PACKAGES)) {
  const pkg = JSON.parse(readFileSync(pkgPath, 'utf8'));
  if (!pkg.name?.startsWith('@kumiki/')) continue;
  if (pkg.private) continue;
  seen.push({ pkgPath, pkg });
}
seen.sort((a, b) => a.pkg.name.localeCompare(b.pkg.name));

for (const { pkgPath, pkg } of seen) {
  const dir = dirname(pkgPath);
  const indexTs = join(dir, 'src', 'index.ts');
  const dtsMts = join(dir, 'dist', 'index.d.mts');
  const dtsTs = join(dir, 'dist', 'index.d.ts');

  sections.push(`## ${pkg.name}`);
  sections.push('');
  if (pkg.description) {
    sections.push(pkg.description);
    sections.push('');
  }

  if (existsSync(indexTs)) {
    const doc = firstJSDoc(readFileSync(indexTs, 'utf8'));
    if (doc) {
      sections.push('### Overview');
      sections.push('');
      sections.push(doc);
      sections.push('');
    }
  }

  let dts = null;
  if (existsSync(dtsMts)) dts = readFileSync(dtsMts, 'utf8');
  else if (existsSync(dtsTs)) dts = readFileSync(dtsTs, 'utf8');
  if (dts) {
    const names = exportedNames(dts);
    if (names.length > 0) {
      sections.push('### Exports');
      sections.push('');
      for (const n of names) sections.push(`- \`${n}\``);
      sections.push('');
    }
  }

  sections.push(`Source: \`${relative(ROOT, dir)}/\``);
  sections.push('');
  sections.push('---');
  sections.push('');
}

writeFileSync(OUT, sections.join('\n'), 'utf8');
console.log(`✓ Wrote ${seen.length} packages to ${relative(ROOT, OUT)}`);
